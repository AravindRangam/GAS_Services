package com.kartha.cssp.data;

public class AddressData {

    private String addressType;
    private String aptNo;
    private String houseNo;
    private String streetName;
    private String city;
    private String state;
    private String country;
    private String zip;
    private String createdAddTs;
    private String updatedAddTs;

    public String getAddressType() {
        return addressType;
    }

    public void setAddressType(String addressType) {
        this.addressType = addressType;
    }

    public String getAptNo() {
        return aptNo;
    }

    public void setAptNo(String aptNo) {
        this.aptNo = aptNo;
    }
    public String getHouseNo() {
        return houseNo;
    }

    public void setHouseNo(String houseNo) {
        this.houseNo = houseNo;
    }

    public String getStreetName() {
        return streetName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getCreatedAddTs() {
        return createdAddTs;
    }

    public void setCreatedAddTs(String createdAddTs) {
        this.createdAddTs = createdAddTs;
    }

    public String getUpdatedAddTs() {
        return updatedAddTs;
    }

    public void setUpdatedAddTs(String updatedAddTs) {
        this.updatedAddTs = updatedAddTs;
    }
}
